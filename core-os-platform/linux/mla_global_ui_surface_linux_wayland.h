static void __linux_wayland_pointer_button(void *data, struct wl_pointer *pointer,
                                             mla_uint32_t serial, mla_uint32_t time,
                                             mla_uint32_t button, mla_uint32_t state) {
    (void)data; (void)pointer; (void)serial; (void)time;
    mla_linux_wayland_surface_t *ws = __linux_wayland_active_surface;
    if (!ws) return;

    mla_bool_t pressed = (state == WL_POINTER_BUTTON_STATE_PRESSED);

    if (button == BTN_LEFT) {
        ws->input.leftButtonDown = pressed;
        if (!pressed && ws->pendingEvents.count < MLA_WAYLAND_MAX_PENDING_EVENTS) {
            mla_ui_surface_input_event_t evt = mla_ui_surface_input_event_empty();
            evt.kind = MLA_UI_SURFACE_INPUT_EVENT_KIND_CLICK;
            evt.click.position.x = ws->input.pointerX;
            evt.click.position.y = ws->input.pointerY;
            evt.click.button = MLA_UI_SURFACE_INPUT_EVENT_CLICK_BUTTON_LEFT;
            ws->pendingEvents.events[ws->pendingEvents.count++] = evt;
        }
    } else if (button == BTN_RIGHT) {
        ws->input.rightButtonDown = pressed;
        if (!pressed && ws->pendingEvents.count < MLA_WAYLAND_MAX_PENDING_EVENTS) {
            mla_ui_surface_input_event_t evt = mla_ui_surface_input_event_empty();
            evt.kind = MLA_UI_SURFACE_INPUT_EVENT_KIND_CLICK;
            evt.click.position.x = ws->input.pointerX;
            evt.click.position.y = ws->input.pointerY;
            evt.click.button = MLA_UI_SURFACE_INPUT_EVENT_CLICK_BUTTON_RIGHT;
            ws->pendingEvents.events[ws->pendingEvents.count++] = evt;
        }
    } else if (button == BTN_MIDDLE) {
        ws->input.middleButtonDown = pressed;
        if (!pressed && ws->pendingEvents.count < MLA_WAYLAND_MAX_PENDING_EVENTS) {
            mla_ui_surface_input_event_t evt = mla_ui_surface_input_event_empty();
            evt.kind = MLA_UI_SURFACE_INPUT_EVENT_KIND_CLICK;
            evt.click.position.x = ws->input.pointerX;
            evt.click.position.y = ws->input.pointerY;
            evt.click.button = MLA_UI_SURFACE_INPUT_EVENT_CLICK_BUTTON_MIDDLE;
            ws->pendingEvents.events[ws->pendingEvents.count++] = evt;
        }
    }
}

