struct mla_linux_wayland_shm_buffer_t {
    struct wl_buffer *buffer;
    mla_uint32_t *data;
    mla_int32_t width;
    mla_int32_t height;
    mla_int32_t stride;
    mla_size_t size;
    mla_bool_t busy;
    mla_int32_t fd;
};

static mla_linux_wayland_shm_buffer_t __linux_wayland_shm_buffer_empty() {
    return { nullptr, nullptr, 0, 0, 0, 0, false, -1 };
}

static void __linux_wayland_shm_buffer_release(void *data, struct wl_buffer *buffer) {
    (void)buffer;
    mla_linux_wayland_shm_buffer_t *shm_buf = static_cast<mla_linux_wayland_shm_buffer_t *>(data);
    shm_buf->busy = false;
}

static const struct wl_buffer_listener __linux_wayland_buffer_listener = {
    __linux_wayland_shm_buffer_release
};

static mla_bool_t __linux_wayland_create_shm_buffer(mla_linux_wayland_shm_buffer_t &buf,
                                                      mla_int32_t width, mla_int32_t height) {
    if (g_wl_shm == nullptr || width <= 0 || height <= 0) return false;

    // Destroy old buffer if exists
    if (buf.buffer) {
        wl_buffer_destroy(buf.buffer);
        buf.buffer = nullptr;
    }
    if (buf.data) {
        munmap(buf.data, buf.size);
        buf.data = nullptr;
    }
    if (buf.fd >= 0) {
        close(buf.fd);
        buf.fd = -1;
    }

    mla_int32_t stride = width * 4; // ARGB8888, 4 bytes per pixel
    mla_size_t size = (mla_size_t)(stride * height);

    // Create anonymous shared memory file
    mla_int32_t fd = -1;
#if defined(__NR_memfd_create) || defined(SYS_memfd_create)
    fd = memfd_create("mla-wayland-shm", MFD_CLOEXEC);
#endif
    if (fd < 0) {
        // Fallback to shm_open
        mla_char_t name[64];
        static mla_uint32_t shm_counter = 0;
        // Build name manually
        name[0] = '/'; name[1] = 'm'; name[2] = 'l'; name[3] = 'a'; name[4] = '-';
        name[5] = 'w'; name[6] = 'l'; name[7] = '-';
        mla_uint32_t c = shm_counter++;
        mla_int32_t pos = 8;
        if (c == 0) { name[pos++] = '0'; }
        else {
            mla_char_t digits[10];
            mla_int32_t dcount = 0;
            while (c > 0) { digits[dcount++] = (mla_char_t)('0' + (c % 10)); c /= 10; }
            for (mla_int32_t d = dcount - 1; d >= 0; d--) name[pos++] = digits[d];
        }
        name[pos] = '\0';

        fd = shm_open(name, O_RDWR | O_CREAT | O_EXCL, 0600);
        if (fd < 0) return false;
        shm_unlink(name);

        // Set close-on-exec flag to prevent fd leaks in forked processes
        fcntl(fd, F_SETFD, FD_CLOEXEC);
    }

    if (ftruncate(fd, (off_t)size) < 0) {
        close(fd);
        return false;
    }

    mla_uint32_t *data = static_cast<mla_uint32_t *>(mmap(nullptr, size, PROT_READ | PROT_WRITE, MAP_SHARED, fd, 0));
    if (data == MAP_FAILED) {
        close(fd);
        return false;
    }

    struct wl_shm_pool *pool = wl_shm_create_pool(g_wl_shm, fd, (mla_int32_t)size);
    if (!pool) {
        munmap(data, size);
        close(fd);
        return false;
    }

    struct wl_buffer *wl_buf = wl_shm_pool_create_buffer(pool, 0, width, height, stride, WL_SHM_FORMAT_ARGB8888);
    wl_shm_pool_destroy(pool);

    if (!wl_buf) {
        munmap(data, size);
        close(fd);
        return false;
    }

    wl_buffer_add_listener(wl_buf, &__linux_wayland_buffer_listener, &buf);

    buf.buffer = wl_buf;
    buf.data = data;
    buf.width = width;
    buf.height = height;
    buf.stride = stride;
    buf.size = size;
    buf.busy = false;
    buf.fd = fd;

    return true;
}

static void __linux_wayland_destroy_shm_buffer(mla_linux_wayland_shm_buffer_t &buf) {
    if (buf.buffer) {
        wl_buffer_destroy(buf.buffer);
        buf.buffer = nullptr;
    }
    if (buf.data) {
        munmap(buf.data, buf.size);
        buf.data = nullptr;
    }
    if (buf.fd >= 0) {
        close(buf.fd);
        buf.fd = -1;
    }
}


