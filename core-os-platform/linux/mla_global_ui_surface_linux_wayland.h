// Scanline fill for a polygon defined by an array of points
static void __linux_wayland_fill_polygon(mla_uint32_t *data, mla_int32_t bufWidth, mla_int32_t bufHeight,
                                           const mla_double_t *xs, const mla_double_t *ys, mla_size_t numPoints,
                                           mla_uint32_t color) {
    if (numPoints < 3) return;

    // Find bounding box
    mla_double_t minY = ys[0], maxY = ys[0];
    for (mla_size_t i = 1; i < numPoints; i++) {
        if (ys[i] < minY) minY = ys[i];
        if (ys[i] > maxY) maxY = ys[i];
    }

    mla_int32_t iy0 = __linux_wayland_iclamp((mla_int32_t)minY, 0, bufHeight);
    mla_int32_t iy1 = __linux_wayland_iclamp((mla_int32_t)maxY + 1, 0, bufHeight);

    // Allocate intersection buffer on stack for reasonable polygon sizes
    mla_double_t intersections[256];

    for (mla_int32_t row = iy0; row < iy1; row++) {
        mla_double_t scanY = (mla_double_t)row + 0.5;
        mla_int32_t numIntersections = 0;

        for (mla_size_t i = 0; i < numPoints && numIntersections < 254; i++) {
            mla_size_t j = (i + 1) % numPoints;
            mla_double_t ey0 = ys[i], ey1 = ys[j];
            mla_double_t ex0 = xs[i], ex1 = xs[j];

            if ((ey0 <= scanY && ey1 > scanY) || (ey1 <= scanY && ey0 > scanY)) {
                mla_double_t t = (scanY - ey0) / (ey1 - ey0);
                intersections[numIntersections++] = ex0 + t * (ex1 - ex0);
            }
        }

        // Sort intersections (simple insertion sort for small arrays)
        for (mla_int32_t i = 1; i < numIntersections; i++) {
            mla_double_t key = intersections[i];
            mla_int32_t j = i - 1;
            while (j >= 0 && intersections[j] > key) {
                intersections[j + 1] = intersections[j];
                j--;
            }
            intersections[j + 1] = key;
        }

        // Fill between pairs of intersections
        for (mla_int32_t i = 0; i + 1 < numIntersections; i += 2) {
            __linux_wayland_fill_hline(data, bufWidth, bufHeight,
                                        (mla_int32_t)intersections[i], (mla_int32_t)intersections[i + 1], row, color);
        }
    }
}

// Stroke polygon outline
static void __linux_wayland_stroke_polygon(mla_uint32_t *data, mla_int32_t bufWidth, mla_int32_t bufHeight,
                                             const mla_double_t *xs, const mla_double_t *ys, mla_size_t numPoints,
                                             mla_double_t strokeWidth, mla_uint32_t strokeColor, mla_bool_t closed) {
    if (numPoints < 2) return;
    mla_size_t limit = closed ? numPoints : numPoints - 1;
    for (mla_size_t i = 0; i < limit; i++) {
        mla_size_t j = (i + 1) % numPoints;
        __linux_wayland_draw_line(data, bufWidth, bufHeight, xs[i], ys[i], xs[j], ys[j], strokeWidth, strokeColor);
    }
}

