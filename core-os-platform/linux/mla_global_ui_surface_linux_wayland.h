// Pack ARGB color into a 32-bit pixel (pre-multiplied alpha, ARGB8888 byte order)
static inline mla_uint32_t __linux_wayland_pack_argb(mla_uint8_t r, mla_uint8_t g, mla_uint8_t b, mla_uint8_t a) {
    // Pre-multiply alpha
    mla_uint32_t pa = (mla_uint32_t)a;
    mla_uint32_t pr = ((mla_uint32_t)r * pa + 127) / 255;
    mla_uint32_t pg = ((mla_uint32_t)g * pa + 127) / 255;
    mla_uint32_t pb = ((mla_uint32_t)b * pa + 127) / 255;
    return (pa << 24) | (pr << 16) | (pg << 8) | pb;
}

static inline mla_uint32_t __linux_wayland_color_to_pixel(const mla_ui_surface_draw_command_color_t &color) {
    return __linux_wayland_pack_argb(color.r, color.g, color.b, color.a);
}

// Alpha-blend source (pre-multiplied) over destination (pre-multiplied)
static inline mla_uint32_t __linux_wayland_blend_pixel(mla_uint32_t dst, mla_uint32_t src) {
    mla_uint32_t sa = (src >> 24) & 0xFF;
    if (sa == 255) return src;
    if (sa == 0) return dst;

    mla_uint32_t inv_sa = 255 - sa;

    mla_uint32_t db = (dst) & 0xFF;
    mla_uint32_t dg = (dst >> 8) & 0xFF;
    mla_uint32_t dr = (dst >> 16) & 0xFF;
    mla_uint32_t da = (dst >> 24) & 0xFF;

    mla_uint32_t sb = (src) & 0xFF;
    mla_uint32_t sg = (src >> 8) & 0xFF;
    mla_uint32_t sr = (src >> 16) & 0xFF;

    mla_uint32_t ob = sb + ((db * inv_sa + 127) / 255);
    mla_uint32_t og = sg + ((dg * inv_sa + 127) / 255);
    mla_uint32_t or_ = sr + ((dr * inv_sa + 127) / 255);
    mla_uint32_t oa = sa + ((da * inv_sa + 127) / 255);

    return (oa << 24) | (or_ << 16) | (og << 8) | ob;
}

// Alpha-blend source with fractional coverage for anti-aliasing
static inline mla_uint32_t __linux_wayland_blend_pixel_coverage(mla_uint32_t dst, mla_uint32_t src, mla_uint32_t coverage) {
    if (coverage == 0) return dst;
    if (coverage >= 255) return __linux_wayland_blend_pixel(dst, src);

    // Modulate source alpha by coverage
    mla_uint32_t sa = ((src >> 24) & 0xFF);
    mla_uint32_t sb = (src) & 0xFF;
    mla_uint32_t sg = (src >> 8) & 0xFF;
    mla_uint32_t sr = (src >> 16) & 0xFF;

    sa = (sa * coverage + 127) / 255;
    sb = (sb * coverage + 127) / 255;
    sg = (sg * coverage + 127) / 255;
    sr = (sr * coverage + 127) / 255;

    mla_uint32_t modulated = (sa << 24) | (sr << 16) | (sg << 8) | sb;
    return __linux_wayland_blend_pixel(dst, modulated);
}

// Set a pixel in the buffer with bounds checking and alpha blending
static inline void __linux_wayland_set_pixel(mla_uint32_t *data, mla_int32_t width, mla_int32_t height,
                                              mla_int32_t x, mla_int32_t y, mla_uint32_t color) {
    if (x < 0 || y < 0 || x >= width || y >= height) return;
    mla_uint32_t *pixel = &data[y * width + x];
    *pixel = __linux_wayland_blend_pixel(*pixel, color);
}

// Set a pixel with coverage (for anti-aliasing)
static inline void __linux_wayland_set_pixel_aa(mla_uint32_t *data, mla_int32_t width, mla_int32_t height,
                                                  mla_int32_t x, mla_int32_t y, mla_uint32_t color, mla_uint32_t coverage) {
    if (x < 0 || y < 0 || x >= width || y >= height) return;
    mla_uint32_t *pixel = &data[y * width + x];
    *pixel = __linux_wayland_blend_pixel_coverage(*pixel, color, coverage);
}

