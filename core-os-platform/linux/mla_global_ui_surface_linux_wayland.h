void __linux_wayland_init() {
    // Connect to Wayland display
    g_wl_display = wl_display_connect(nullptr);
    if (!g_wl_display) {
        mla_warning("Failed to connect to Wayland display.");
        return;
    }

    // Get registry and bind globals
    g_wl_registry = wl_display_get_registry(g_wl_display);
    wl_registry_add_listener(g_wl_registry, &__linux_wayland_registry_listener, nullptr);
    wl_display_roundtrip(g_wl_display);

    // Second roundtrip to receive wl_output.scale and other deferred events
    wl_display_roundtrip(g_wl_display);

    // Initialize FreeType
    if (FT_Init_FreeType(&g_ft_library) != 0) {
        mla_warning("Failed to initialize FreeType library.");
        g_ft_library = nullptr;
    }

    // Initialize Fontconfig
    g_fc_config = FcInitLoadConfigAndFonts();
    if (!g_fc_config) {
        mla_warning("Failed to initialize Fontconfig.");
    }
}

void __linux_wayland_shutdown() {
    // Cleanup Fontconfig
    if (g_fc_config) {
        FcConfigDestroy(g_fc_config);
        g_fc_config = nullptr;
    }
    FcFini();

    // Cleanup FreeType
    if (g_ft_library) {
        FT_Done_FreeType(g_ft_library);
        g_ft_library = nullptr;
    }

    // Cleanup Wayland globals
    if (g_wl_seat) { wl_seat_destroy(g_wl_seat); g_wl_seat = nullptr; }
    if (g_wl_output) { wl_output_destroy(g_wl_output); g_wl_output = nullptr; }
    if (g_xdg_wm_base) { xdg_wm_base_destroy(g_xdg_wm_base); g_xdg_wm_base = nullptr; }
    if (g_wl_shm) { wl_shm_destroy(g_wl_shm); g_wl_shm = nullptr; }
    if (g_wl_compositor) { wl_compositor_destroy(g_wl_compositor); g_wl_compositor = nullptr; }
    if (g_wl_registry) { wl_registry_destroy(g_wl_registry); g_wl_registry = nullptr; }
    if (g_wl_display) { wl_display_disconnect(g_wl_display); g_wl_display = nullptr; }
}

struct MlaWaylandAutoInit {
    MlaWaylandAutoInit() { __linux_wayland_init(); }
    ~MlaWaylandAutoInit() { __linux_wayland_shutdown(); }
};

// Single global instance
static MlaWaylandAutoInit g_mlaWaylandAutoInit;


