struct mla_linux_wayland_font_cache_item_t {
    mla_ui_surface_font_type_t font_type;
    mla_int32_t cachedScale; // DPI scale at which this face was configured
    FT_Face face;
    mla_buffer_reference_t faceOwner;
};

struct mla_linux_wayland_font_cache_item_initializer_t {
    static mla_linux_wayland_font_cache_item_t init() {
        return {
            mla_ui_surface_font_type_empty(),
            1,
            nullptr,
            mla_buffer_reference_noOwner()
        };
    }
};

mla_buffer_cleanup_mode __linux_wayland_font_face_cleanup(mla_platform_pointer_t data, const mla_dynamic_data_t &userData) {
    (void)userData;
    FT_Face face = static_cast<FT_Face>(data);
    if (face) {
        FT_Done_Face(face);
    }
    return CLEAN_UP_SKIP;
}

// Find a font file path using Fontconfig
static mla_bool_t __linux_wayland_find_font_path(const mla_char_t *family, mla_bool_t bold, mla_bool_t italic,
                                                   mla_char_t *outPath, mla_size_t outPathSize) {
    if (g_fc_config == nullptr) return false;

    FcPattern *pattern = FcPatternCreate();
    if (!pattern) return false;

    FcPatternAddString(pattern, FC_FAMILY, (const FcChar8 *)family);

    if (bold) {
        FcPatternAddInteger(pattern, FC_WEIGHT, FC_WEIGHT_BOLD);
    }

    if (italic) {
        FcPatternAddInteger(pattern, FC_SLANT, FC_SLANT_ITALIC);
    }

    FcConfigSubstitute(g_fc_config, pattern, FcMatchPattern);
    FcDefaultSubstitute(pattern);

    FcResult result;
    FcPattern *match = FcFontMatch(g_fc_config, pattern, &result);

    mla_bool_t found = false;
    if (match) {
        FcChar8 *file = nullptr;
        if (FcPatternGetString(match, FC_FILE, 0, &file) == FcResultMatch && file) {
            mla_size_t len = 0;
            while (file[len] != 0) len++;
            if (len < outPathSize) {
                mla_memcpy(outPath, file, len);
                outPath[len] = '\0';
                found = true;
            }
        }
        FcPatternDestroy(match);
    }

    FcPatternDestroy(pattern);
    return found;
}

static FT_Face __linux_wayland_font_cache_get_or_create(
    mla_array_list_t<mla_linux_wayland_font_cache_item_t, mla_linux_wayland_font_cache_item_initializer_t> &cache,
    const mla_ui_surface_font_type_t &fontType) {

    mla_int32_t currentScale = g_wl_output_scale;

    if (mla_string_is_empty(fontType.family) || g_ft_library == nullptr)
        return nullptr;

    // Check cache (must match font type AND scale factor for crisp HiDPI text)
    for (mla_size_t i = 0; i < mla_array_list_size(cache); i++) {
        const mla_linux_wayland_font_cache_item_t &item = mla_array_list_get_unsafe(cache, i);
        if (!mla_ui_surface_font_type_equals(item.font_type, fontType))
            continue;
        if (item.cachedScale != currentScale)
            continue;
        return item.face;
    }

    // Find font file using Fontconfig
    mla_c_string_t familyCStr = mla_string_to_cString(fontType.family);

    mla_char_t fontPath[512];
    mla_bool_t found = __linux_wayland_find_font_path(familyCStr.c_str, fontType.bold, fontType.italic, fontPath, 512);
    mla_destroy_c_string(familyCStr);

    if (!found) {
        // Fallback: try sans-serif
        found = __linux_wayland_find_font_path("sans-serif", fontType.bold, fontType.italic, fontPath, 512);
        if (!found) return nullptr;
    }

    // Load font face
    FT_Face face = nullptr;
    if (FT_New_Face(g_ft_library, fontPath, 0, &face) != 0) {
        return nullptr;
    }

    // Set character size (in 1/64th of points) at scaled DPI for crisp HiDPI rendering
    mla_uint32_t scaledDpi = (mla_uint32_t)(96 * currentScale);
    FT_Set_Char_Size(face, 0, (FT_F26Dot6)(fontType.size * 64.0), scaledDpi, scaledDpi);

    // Add to cache
    mla_linux_wayland_font_cache_item_t newItem = {
        fontType,
        currentScale,
        face,
        mla_buffer_reference_create(face, true, __linux_wayland_font_face_cleanup, mla_dynamic_data_empty())
    };

    if (mla_array_list_size(cache) < mla_global_ui_surface_linux_wayland_font_cache_size) {
        mla_array_list_add(cache, newItem);
    } else {
        mla_linux_wayland_font_cache_item_t &oldItem = mla_array_list_get_unsafe(cache, 0);
        oldItem = newItem;
    }

    return face;
}


