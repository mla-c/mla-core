static inline mla_double_t __linux_wayland_abs(mla_double_t x) {
    return x < 0.0 ? -x : x;
}

static inline mla_double_t __linux_wayland_min(mla_double_t a, mla_double_t b) {
    return a < b ? a : b;
}

static inline mla_double_t __linux_wayland_max(mla_double_t a, mla_double_t b) {
    return a > b ? a : b;
}

static inline mla_double_t __linux_wayland_clamp(mla_double_t val, mla_double_t lo, mla_double_t hi) {
    if (val < lo) return lo;
    if (val > hi) return hi;
    return val;
}

static inline mla_int32_t __linux_wayland_iclamp(mla_int32_t val, mla_int32_t lo, mla_int32_t hi) {
    if (val < lo) return lo;
    if (val > hi) return hi;
    return val;
}

// Compare two C strings for equality without including <string.h>
static inline mla_bool_t __linux_wayland_str_eq(const char *a, const char *b) {
    while (*a && *b) { if (*a != *b) return false; a++; b++; }
    return *a == *b;
}

