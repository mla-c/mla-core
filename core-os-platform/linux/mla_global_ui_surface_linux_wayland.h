mla_buffer_cleanup_mode __linux_wayland_surface_buffer_cleanup(mla_platform_pointer_t data, const mla_dynamic_data_t &userData) {
    (void)userData;

    mla_linux_wayland_surface_t *ws = static_cast<mla_linux_wayland_surface_t *>(data);

    if (ws != nullptr) {
        // Cleanup render cache
        ws->renderCache = __linux_wayland_render_cache_empty();

        // Cleanup input
        if (ws->input.pointer) { wl_pointer_destroy(ws->input.pointer); ws->input.pointer = nullptr; }
        if (ws->input.keyboard) { wl_keyboard_destroy(ws->input.keyboard); ws->input.keyboard = nullptr; }
        if (ws->input.xkbState) { xkb_state_unref(ws->input.xkbState); ws->input.xkbState = nullptr; }
        if (ws->input.xkbKeymap) { xkb_keymap_unref(ws->input.xkbKeymap); ws->input.xkbKeymap = nullptr; }
        if (ws->input.xkbContext) { xkb_context_unref(ws->input.xkbContext); ws->input.xkbContext = nullptr; }

        // Cleanup SHM buffers
        __linux_wayland_destroy_shm_buffer(ws->buffers[0]);
        __linux_wayland_destroy_shm_buffer(ws->buffers[1]);

        // Cleanup Wayland objects
        if (ws->xdg_toplevel) { xdg_toplevel_destroy(ws->xdg_toplevel); ws->xdg_toplevel = nullptr; }
        if (ws->xdg_surface) { xdg_surface_destroy(ws->xdg_surface); ws->xdg_surface = nullptr; }
        if (ws->wl_surface) { wl_surface_destroy(ws->wl_surface); ws->wl_surface = nullptr; }

        if (__linux_wayland_active_surface == ws) {
            __linux_wayland_active_surface = nullptr;
        }
    }

    return CLEAN_UP_NEEDED;
}

mla_bool_t __linux_wayland_create_surface(mla_ui_surface_t &outSurface) {
    mla_linux_wayland_surface_t *ws = static_cast<mla_linux_wayland_surface_t *>(
        mla_platform_malloc(sizeof(mla_linux_wayland_surface_t)));

    if (ws == nullptr) return false;

    mla_memset(ws, 0, sizeof(mla_linux_wayland_surface_t));
    ws->is_initialized = false;
    ws->default_size = {0, 0};
    ws->buffers[0] = __linux_wayland_shm_buffer_empty();
    ws->buffers[1] = __linux_wayland_shm_buffer_empty();
    ws->currentBuffer = 0;
    ws->bufferScale = g_wl_output_scale > 0 ? g_wl_output_scale : 1;
    ws->renderCache = __linux_wayland_render_cache_empty();
    ws->input = __linux_wayland_input_state_empty();
    ws->pendingEvents = __linux_wayland_pending_events_empty();
    ws->pendingWidth = 0;
    ws->pendingHeight = 0;
    ws->hasPendingConfigure = false;
    ws->closed = false;

#ifdef mla_debug_build
    ws->DEBUG_last_fps_time = 0;
    ws->DEBUG_frames_accumulated = 0;
    ws->DEBUG_current_fps = 0;
#endif

    outSurface.resource = ws;
    outSurface.resourceOwner = mla_buffer_reference_create(ws, true,
        __linux_wayland_surface_buffer_cleanup, mla_dynamic_data_empty());
    outSurface.get_size = __linux_wayland_surface_get_size;
    outSurface.set_size = __linux_wayland_surface_set_size;
    outSurface.render_draw_commands = __linux_wayland_surface_render_draw_commands;
    outSurface.calc_text_size = __linux_wayland_surface_calc_text_size;
    outSurface.get_input_states = __linux_wayland_surface_input_states;

    return true;
}

mla_ui_display_surface_low_level_access_t g_ui_display_surface_low_level_access = {
    __linux_wayland_create_surface
};

