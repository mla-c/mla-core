static mla_bool_t __linux_wayland_create_window(mla_linux_wayland_surface_t *ws) {
    if (g_wl_display == nullptr || g_wl_compositor == nullptr || g_xdg_wm_base == nullptr) {
        mla_warning("Wayland not initialized or missing required globals.");
        return false;
    }

    // Create Wayland surface
    ws->wl_surface = wl_compositor_create_surface(g_wl_compositor);
    if (!ws->wl_surface) {
        mla_warning("Failed to create Wayland surface.");
        return false;
    }

    // Create xdg_surface
    ws->xdg_surface = xdg_wm_base_get_xdg_surface(g_xdg_wm_base, ws->wl_surface);
    if (!ws->xdg_surface) {
        mla_warning("Failed to create xdg_surface.");
        wl_surface_destroy(ws->wl_surface);
        ws->wl_surface = nullptr;
        return false;
    }
    xdg_surface_add_listener(ws->xdg_surface, &__linux_wayland_xdg_surface_listener, nullptr);

    // Create xdg_toplevel
    ws->xdg_toplevel = xdg_surface_get_toplevel(ws->xdg_surface);
    if (!ws->xdg_toplevel) {
        mla_warning("Failed to create xdg_toplevel.");
        xdg_surface_destroy(ws->xdg_surface);
        wl_surface_destroy(ws->wl_surface);
        ws->xdg_surface = nullptr;
        ws->wl_surface = nullptr;
        return false;
    }
    xdg_toplevel_add_listener(ws->xdg_toplevel, &__linux_wayland_xdg_toplevel_listener, nullptr);
    xdg_toplevel_set_title(ws->xdg_toplevel, "MLA App");
    xdg_toplevel_set_app_id(ws->xdg_toplevel, "mla.app");

    // Set active surface for event routing
    __linux_wayland_active_surface = ws;

    // Initial commit to get the configure event
    wl_surface_commit(ws->wl_surface);

    // Roundtrip to process configure and output scale events
    wl_display_roundtrip(g_wl_display);

    // High DPI: apply the compositor's output scale factor
    ws->bufferScale = g_wl_output_scale;
    if (ws->bufferScale < 1) ws->bufferScale = 1;
    wl_surface_set_buffer_scale(ws->wl_surface, ws->bufferScale);

    // Set default logical size if compositor didn't configure one
    mla_int32_t w = ws->pendingWidth;
    mla_int32_t h = ws->pendingHeight;
    if (w <= 0) w = (ws->default_size.width > 0) ? (mla_int32_t)ws->default_size.width : 800;
    if (h <= 0) h = (ws->default_size.height > 0) ? (mla_int32_t)ws->default_size.height : 600;
    ws->pendingWidth = w;
    ws->pendingHeight = h;

    // Create initial SHM buffers at physical pixel dimensions
    mla_int32_t physW = w * ws->bufferScale;
    mla_int32_t physH = h * ws->bufferScale;
    if (!__linux_wayland_create_shm_buffer(ws->buffers[0], physW, physH) ||
        !__linux_wayland_create_shm_buffer(ws->buffers[1], physW, physH)) {
        mla_warning("Failed to create SHM buffers.");
        return false;
    }

    // Initialize xkb
    ws->input.xkbContext = xkb_context_new(XKB_CONTEXT_NO_FLAGS);

    ws->is_initialized = true;
    ws->currentBuffer = 0;

    return true;
}


//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Render Draw Commands
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

mla_bool_t __linux_wayland_surface_render_draw_commands(const mla_ui_surface_t &surface,
    const mla_array_list_t<mla_ui_surface_draw_command_t, mla_ui_surface_draw_command_initializer_t> &drawCommands,
    mla_array_list_t<mla_ui_surface_input_event_t, mla_ui_surface_input_event_initializer_t> &eventsSinceLastFrame,
    mla_uint64_t timeSinceLastFrameMs) {

    mla_linux_wayland_surface_t *ws = static_cast<mla_linux_wayland_surface_t *>(surface.resource);
    if (ws == nullptr) return false;

    if (!ws->is_initialized) {
        if (!__linux_wayland_create_window(ws)) {
            mla_warning("Failed to initialize Wayland UI surface for drawing.");
            return false;
        }
    }

    if (ws->closed) return false;

    // Dispatch Wayland events (non-blocking)
    wl_display_dispatch_pending(g_wl_display);
    wl_display_flush(g_wl_display);

    // Process any pending Wayland events from the fd
    struct wl_display *dpy = g_wl_display;
    while (wl_display_prepare_read(dpy) != 0) {
        wl_display_dispatch_pending(dpy);
    }
    wl_display_read_events(dpy);
    wl_display_dispatch_pending(dpy);

    // Drain pending input events into the caller's event list
    for (mla_size_t i = 0; i < ws->pendingEvents.count; i++) {
        mla_array_list_add(eventsSinceLastFrame, ws->pendingEvents.events[i]);
    }
    ws->pendingEvents.count = 0;

    // High DPI: check if output scale changed at runtime (e.g. window moved to different monitor)
    mla_int32_t newScale = g_wl_output_scale;
    if (newScale < 1) newScale = 1;
    if (newScale != ws->bufferScale) {
        ws->bufferScale = newScale;
        wl_surface_set_buffer_scale(ws->wl_surface, ws->bufferScale);
        // Force buffer recreation at new physical size
        ws->pendingWidth = ws->buffers[ws->currentBuffer].width / ws->bufferScale;
        ws->pendingHeight = ws->buffers[ws->currentBuffer].height / ws->bufferScale;
        if (ws->pendingWidth <= 0) ws->pendingWidth = 1;
        if (ws->pendingHeight <= 0) ws->pendingHeight = 1;
    }

    // Handle resize if pending (pendingWidth/Height are in logical coordinates)
    mla_int32_t scale = ws->bufferScale;
    mla_int32_t curW = ws->buffers[ws->currentBuffer].width;
    mla_int32_t curH = ws->buffers[ws->currentBuffer].height;
    mla_int32_t newPhysW = ws->pendingWidth * scale;
    mla_int32_t newPhysH = ws->pendingHeight * scale;

    if (newPhysW > 0 && newPhysH > 0 && (newPhysW != curW || newPhysH != curH)) {
        // Wait for current buffers to not be busy
        for (mla_int32_t b = 0; b < 2; b++) {
            if (ws->buffers[b].busy) {
                wl_display_roundtrip(g_wl_display);
            }
        }
        __linux_wayland_create_shm_buffer(ws->buffers[0], newPhysW, newPhysH);
        __linux_wayland_create_shm_buffer(ws->buffers[1], newPhysW, newPhysH);
        ws->currentBuffer = 0;
    }

    // FPS throttling
    if (timeSinceLastFrameMs < (mla_uint64_t)(1000 * 0.75 / mla_global_ui_surface_linux_wayland_fps_target)) {
        return false;
    }

    // Select back buffer for drawing
    mla_int32_t backIdx = ws->currentBuffer;
    mla_linux_wayland_shm_buffer_t &backBuf = ws->buffers[backIdx];

    if (backBuf.busy) {
        // Buffer still in use by compositor, skip this frame
        return false;
    }

    mla_uint32_t *pixels = backBuf.data;
    mla_int32_t bufW = backBuf.width;
    mla_int32_t bufH = backBuf.height;

    if (!pixels || bufW <= 0 || bufH <= 0) return false;

    // Clear to white (opaque)
    mla_uint32_t clearColor = 0xFFFFFFFF; // ARGB: fully opaque white
    mla_size_t pixelCount = (mla_size_t)(bufW * bufH);
    for (mla_size_t i = 0; i < pixelCount; i++) {
        pixels[i] = clearColor;
    }

    // Track gradient state
    mla_bool_t hasLinearGradient = false;
    mla_bool_t hasRadialGradient = false;
    mla_double_t lgX1 = 0, lgY1 = 0, lgX2 = 0, lgY2 = 0;
    mla_double_t rgCx = 0, rgCy = 0, rgR = 0;

    // High DPI: scale factor to convert logical coordinates to physical pixels
    mla_double_t s = (mla_double_t)ws->bufferScale;

    // Process draw commands
    for (mla_size_t i = 0; i < mla_array_list_size(drawCommands); i++) {
        const mla_ui_surface_draw_command_t &cmd = mla_array_list_get_unsafe(drawCommands, i);

        switch (cmd.kind) {
            case MLA_UI_SURFACE_DRAW_COMMAND_KIND_RECT: {
                const auto &rect = cmd.rect;
                mla_uint32_t fillColor = __linux_wayland_color_to_pixel(rect.color);

                if (rect.rx > 0 || rect.ry > 0) {
                    __linux_wayland_fill_rounded_rect(pixels, bufW, bufH,
                        rect.x * s, rect.y * s, rect.width * s, rect.height * s,
                        rect.rx * s, rect.ry * s, fillColor);
                } else {
                    __linux_wayland_fill_rect(pixels, bufW, bufH,
                        rect.x * s, rect.y * s, rect.width * s, rect.height * s, fillColor);
                }

                if (rect.stroke_width > 0) {
                    mla_uint32_t strokeColor = __linux_wayland_color_to_pixel(rect.stroke);
                    __linux_wayland_stroke_rect(pixels, bufW, bufH,
                        rect.x * s, rect.y * s, rect.width * s, rect.height * s,
                        rect.rx * s, rect.ry * s, rect.stroke_width * s, strokeColor);
                }
                break;
            }

            case MLA_UI_SURFACE_DRAW_COMMAND_KIND_CIRCLE: {
                const auto &circle = cmd.circle;
                mla_uint32_t fillColor = __linux_wayland_color_to_pixel(circle.fill);
                __linux_wayland_fill_ellipse(pixels, bufW, bufH,
                    circle.cx * s, circle.cy * s, circle.r * s, circle.r * s, fillColor);

                if (circle.stroke_width > 0) {
                    mla_uint32_t strokeColor = __linux_wayland_color_to_pixel(circle.stroke);
                    __linux_wayland_stroke_ellipse(pixels, bufW, bufH,
                        circle.cx * s, circle.cy * s, circle.r * s, circle.r * s,
                        circle.stroke_width * s, strokeColor);
                }
                break;
            }

            case MLA_UI_SURFACE_DRAW_COMMAND_KIND_ELLIPSE: {
                const auto &ell = cmd.ellipse;
                mla_uint32_t fillColor = __linux_wayland_color_to_pixel(ell.fill);
                __linux_wayland_fill_ellipse(pixels, bufW, bufH,
                    ell.cx * s, ell.cy * s, ell.rx * s, ell.ry * s, fillColor);

                if (ell.stroke_width > 0) {
                    mla_uint32_t strokeColor = __linux_wayland_color_to_pixel(ell.stroke);
                    __linux_wayland_stroke_ellipse(pixels, bufW, bufH,
                        ell.cx * s, ell.cy * s, ell.rx * s, ell.ry * s,
                        ell.stroke_width * s, strokeColor);
                }
                break;
            }

            case MLA_UI_SURFACE_DRAW_COMMAND_KIND_LINE: {
                const auto &line = cmd.line;
                mla_uint32_t strokeColor = __linux_wayland_color_to_pixel(line.stroke);
                __linux_wayland_draw_line(pixels, bufW, bufH,
                    line.x1 * s, line.y1 * s, line.x2 * s, line.y2 * s,
                    line.stroke_width * s, strokeColor);
                break;
            }

            case MLA_UI_SURFACE_DRAW_COMMAND_KIND_POLYLINE: {
                const auto &polyline = cmd.polyline;
                mla_size_t numPoints = mla_array_list_size(polyline.points);
                if (numPoints < 2) break;

                mla_double_t *xs = static_cast<mla_double_t *>(mla_platform_malloc(numPoints * sizeof(mla_double_t)));
                mla_double_t *ys = static_cast<mla_double_t *>(mla_platform_malloc(numPoints * sizeof(mla_double_t)));
                if (!xs || !ys) { if (xs) mla_platform_free(xs); if (ys) mla_platform_free(ys); break; }

                for (mla_size_t j = 0; j < numPoints; j++) {
                    const mla_ui_surface_draw_point_t &pt = mla_array_list_get_unsafe(polyline.points, j);
                    xs[j] = pt.x * s;
                    ys[j] = pt.y * s;
                }

                mla_uint32_t strokeColor = __linux_wayland_color_to_pixel(polyline.stroke);
                __linux_wayland_stroke_polygon(pixels, bufW, bufH, xs, ys, numPoints,
                    polyline.stroke_width * s, strokeColor, false);

                mla_platform_free(xs);
                mla_platform_free(ys);
                break;
            }

            case MLA_UI_SURFACE_DRAW_COMMAND_KIND_POLYGON: {
                const auto &polygon = cmd.polygon;
                mla_size_t numPoints = mla_array_list_size(polygon.points);
                if (numPoints < 3) break;

                mla_double_t *xs = static_cast<mla_double_t *>(mla_platform_malloc(numPoints * sizeof(mla_double_t)));
                mla_double_t *ys = static_cast<mla_double_t *>(mla_platform_malloc(numPoints * sizeof(mla_double_t)));
                if (!xs || !ys) { if (xs) mla_platform_free(xs); if (ys) mla_platform_free(ys); break; }

                for (mla_size_t j = 0; j < numPoints; j++) {
                    const mla_ui_surface_draw_point_t &pt = mla_array_list_get_unsafe(polygon.points, j);
                    xs[j] = pt.x * s;
                    ys[j] = pt.y * s;
                }

                mla_uint32_t fillColor = __linux_wayland_color_to_pixel(polygon.fill);
                __linux_wayland_fill_polygon(pixels, bufW, bufH, xs, ys, numPoints, fillColor);

                if (polygon.stroke_width > 0) {
                    mla_uint32_t strokeColor = __linux_wayland_color_to_pixel(polygon.stroke);
                    __linux_wayland_stroke_polygon(pixels, bufW, bufH, xs, ys, numPoints,
                        polygon.stroke_width * s, strokeColor, true);
                }

                mla_platform_free(xs);
                mla_platform_free(ys);
                break;
            }

            case MLA_UI_SURFACE_DRAW_COMMAND_KIND_PATH: {
                const auto &path = cmd.path;
                mla_size_t numCmds = mla_array_list_size(path.commands);
                if (numCmds == 0) break;

                // Flatten path to points (all coordinates scaled to physical pixels)
                mla_size_t maxPts = numCmds * (mla_global_ui_surface_linux_wayland_bezier_segments + 2);
                if (maxPts > 16384) maxPts = 16384;

                mla_double_t *xs = static_cast<mla_double_t *>(mla_platform_malloc(maxPts * sizeof(mla_double_t)));
                mla_double_t *ys = static_cast<mla_double_t *>(mla_platform_malloc(maxPts * sizeof(mla_double_t)));
                if (!xs || !ys) { if (xs) mla_platform_free(xs); if (ys) mla_platform_free(ys); break; }

                mla_size_t ptCount = 0;
                mla_double_t curX = 0, curY = 0;
                mla_size_t subpathStart = 0;

                for (mla_size_t j = 0; j < numCmds; j++) {
                    const auto &pc = mla_array_list_get_unsafe(path.commands, j);

                    switch (pc.kind) {
                        case MLA_UI_SURFACE_DRAW_PATH_COMMAND_MOVE_TO:
                            // If we have a subpath, draw it
                            if (ptCount > subpathStart + 1) {
                                mla_uint32_t fc = __linux_wayland_color_to_pixel(path.fill);
                                __linux_wayland_fill_polygon(pixels, bufW, bufH,
                                    xs + subpathStart, ys + subpathStart,
                                    ptCount - subpathStart, fc);
                                if (path.stroke_width > 0) {
                                    mla_uint32_t sc = __linux_wayland_color_to_pixel(path.stroke);
                                    __linux_wayland_stroke_polygon(pixels, bufW, bufH,
                                        xs + subpathStart, ys + subpathStart,
                                        ptCount - subpathStart, path.stroke_width * s, sc, false);
                                }
                            }
                            subpathStart = ptCount;
                            curX = pc.move_to.x * s;
                            curY = pc.move_to.y * s;
                            if (ptCount < maxPts) { xs[ptCount] = curX; ys[ptCount] = curY; ptCount++; }
                            break;

                        case MLA_UI_SURFACE_DRAW_PATH_COMMAND_LINE_TO:
                            curX = pc.line_to.x * s;
                            curY = pc.line_to.y * s;
                            if (ptCount < maxPts) { xs[ptCount] = curX; ys[ptCount] = curY; ptCount++; }
                            break;

                        case MLA_UI_SURFACE_DRAW_PATH_COMMAND_QUADRATIC_CURVE_TO:
                            __linux_wayland_flatten_quadratic_bezier(
                                curX, curY,
                                pc.quadratic_curve_to.cpx * s, pc.quadratic_curve_to.cpy * s,
                                pc.quadratic_curve_to.x * s, pc.quadratic_curve_to.y * s,
                                xs, ys, &ptCount, maxPts);
                            curX = pc.quadratic_curve_to.x * s;
                            curY = pc.quadratic_curve_to.y * s;
                            break;

                        case MLA_UI_SURFACE_DRAW_PATH_COMMAND_CUBIC_CURVE_TO:
                            __linux_wayland_flatten_cubic_bezier(
                                curX, curY,
                                pc.cubic_curve_to.cp1x * s, pc.cubic_curve_to.cp1y * s,
                                pc.cubic_curve_to.cp2x * s, pc.cubic_curve_to.cp2y * s,
                                pc.cubic_curve_to.x * s, pc.cubic_curve_to.y * s,
                                xs, ys, &ptCount, maxPts);
                            curX = pc.cubic_curve_to.x * s;
                            curY = pc.cubic_curve_to.y * s;
                            break;

                        case MLA_UI_SURFACE_DRAW_PATH_COMMAND_ARC_TO:
                            __linux_wayland_flatten_arc(
                                curX, curY,
                                pc.arc_to.rx * s, pc.arc_to.ry * s,
                                pc.arc_to.x_axis_rotation,
                                pc.arc_to.large_arc_flag, pc.arc_to.sweep_flag,
                                pc.arc_to.x * s, pc.arc_to.y * s,
                                xs, ys, &ptCount, maxPts);
                            curX = pc.arc_to.x * s;
                            curY = pc.arc_to.y * s;
                            break;

                        case MLA_UI_SURFACE_DRAW_PATH_COMMAND_CLOSE_PATH:
                            // Close by connecting back to subpath start
                            if (ptCount > subpathStart && subpathStart < maxPts) {
                                if (ptCount < maxPts) {
                                    xs[ptCount] = xs[subpathStart];
                                    ys[ptCount] = ys[subpathStart];
                                    ptCount++;
                                }
                            }
                            break;
                    }
                }

                // Draw remaining subpath
                if (ptCount > subpathStart + 1) {
                    mla_uint32_t fc = __linux_wayland_color_to_pixel(path.fill);
                    __linux_wayland_fill_polygon(pixels, bufW, bufH,
                        xs + subpathStart, ys + subpathStart,
                        ptCount - subpathStart, fc);
                    if (path.stroke_width > 0) {
                        mla_uint32_t sc = __linux_wayland_color_to_pixel(path.stroke);
                        __linux_wayland_stroke_polygon(pixels, bufW, bufH,
                            xs + subpathStart, ys + subpathStart,
                            ptCount - subpathStart, path.stroke_width * s, sc, false);
                    }
                }

                mla_platform_free(xs);
                mla_platform_free(ys);
                break;
            }

            case MLA_UI_SURFACE_DRAW_COMMAND_KIND_TEXT: {
                if (mla_string_is_empty(cmd.text.content)) break;

                // Font cache returns faces at scaled DPI, so text position must be in physical pixels
                FT_Face face = __linux_wayland_font_cache_get_or_create(
                    ws->renderCache.fontCache, cmd.text.font_type);

                if (face) {
                    mla_uint32_t textColor = __linux_wayland_color_to_pixel(cmd.text.fill);
                    const mla_char_t *textData = mla_string_data(cmd.text.content);
                    mla_size_t textLen = mla_string_length(cmd.text.content);

                    __linux_wayland_draw_text(pixels, bufW, bufH,
                        face, textData, textLen,
                        cmd.text.x * s, cmd.text.y * s, textColor);
                }
                break;
            }

            case MLA_UI_SURFACE_DRAW_COMMAND_KIND_LINEAR_GRADIENT: {
                const auto &lg = cmd.linear_gradient;
                hasLinearGradient = true;
                hasRadialGradient = false;
                lgX1 = lg.x1 * s; lgY1 = lg.y1 * s;
                lgX2 = lg.x2 * s; lgY2 = lg.y2 * s;
                break;
            }

            case MLA_UI_SURFACE_DRAW_COMMAND_KIND_RADIAL_GRADIENT: {
                const auto &rg = cmd.radial_gradient;
                hasLinearGradient = false;
                hasRadialGradient = true;
                rgCx = rg.cx * s; rgCy = rg.cy * s; rgR = rg.r * s;
                break;
            }

            case MLA_UI_SURFACE_DRAW_COMMAND_KIND_STOP: {
                // Gradient stops - simplified implementation
                // Note: Full gradient support would require collecting all stops before rendering
                (void)hasLinearGradient;
                (void)hasRadialGradient;
                (void)lgX1; (void)lgY1; (void)lgX2; (void)lgY2;
                (void)rgCx; (void)rgCy; (void)rgR;
                break;
            }

            default:
                break;
        }
    }

#ifdef mla_debug_build
    // Debug FPS counter
    mla_uint64_t currentTime = __linux_system_time_ms();
    if (ws->DEBUG_last_fps_time == 0) {
        ws->DEBUG_last_fps_time = currentTime;
    }

    ws->DEBUG_frames_accumulated++;

    if (currentTime - ws->DEBUG_last_fps_time >= 1000) {
        ws->DEBUG_current_fps = ws->DEBUG_frames_accumulated;
        ws->DEBUG_frames_accumulated = 0;
        ws->DEBUG_last_fps_time = currentTime;
    }

    // Draw FPS counter
    mla_ui_surface_font_type_t debugFontType = mla_ui_surface_font_type_empty();
    debugFontType.family = mla_string_const("sans-serif");
    debugFontType.size = 15.0;

    FT_Face debugFace = __linux_wayland_font_cache_get_or_create(ws->renderCache.fontCache, debugFontType);
    if (debugFace) {
        mla_char_t fpsText[32];
        // Manual int-to-string for "FPS: NNN"
        fpsText[0] = 'F'; fpsText[1] = 'P'; fpsText[2] = 'S'; fpsText[3] = ':'; fpsText[4] = ' ';
        mla_int32_t fps = ws->DEBUG_current_fps;
        mla_int32_t pos = 5;
        if (fps == 0) { fpsText[pos++] = '0'; }
        else {
            mla_char_t digits[10];
            mla_int32_t dcount = 0;
            while (fps > 0) { digits[dcount++] = (mla_char_t)('0' + (fps % 10)); fps /= 10; }
            for (mla_int32_t d = dcount - 1; d >= 0; d--) fpsText[pos++] = digits[d];
        }
        fpsText[pos] = '\0';

        mla_uint32_t redColor = __linux_wayland_pack_argb(255, 0, 0, 255);
        __linux_wayland_draw_text(pixels, bufW, bufH, debugFace, fpsText, (mla_size_t)pos,
            (mla_double_t)(bufW - 75), (mla_double_t)(bufH - 25), redColor);
    }
#endif

    // Attach buffer to surface and commit
    wl_surface_attach(ws->wl_surface, backBuf.buffer, 0, 0);
    wl_surface_damage_buffer(ws->wl_surface, 0, 0, bufW, bufH);
    wl_surface_commit(ws->wl_surface);
    backBuf.busy = true;

    // Swap to next buffer
    ws->currentBuffer = (ws->currentBuffer + 1) % 2;

    wl_display_flush(g_wl_display);

    return true;
}


