mla_ui_surface_size_t __linux_wayland_surface_get_size(const mla_ui_surface_t &surface) {
    mla_ui_surface_size_t size = {0, 0};

    mla_linux_wayland_surface_t *ws = static_cast<mla_linux_wayland_surface_t *>(surface.resource);
    if (ws == nullptr) return size;

    if (!ws->is_initialized) return ws->default_size;

    // Return logical size (physical buffer size / scale factor)
    mla_linux_wayland_shm_buffer_t &buf = ws->buffers[ws->currentBuffer];
    if (buf.width > 0 && buf.height > 0) {
        mla_int32_t scale = ws->bufferScale;
        if (scale < 1) scale = 1;
        size.width = (mla_uint32_t)(buf.width / scale);
        size.height = (mla_uint32_t)(buf.height / scale);
    } else {
        size = ws->default_size;
    }

    return size;
}

mla_bool_t __linux_wayland_surface_set_size(const mla_ui_surface_t &surface, mla_ui_surface_size_t size) {
    mla_linux_wayland_surface_t *ws = static_cast<mla_linux_wayland_surface_t *>(surface.resource);
    if (ws == nullptr) return false;

    ws->default_size = size;

    if (!ws->is_initialized) return true;

    // Pending dimensions are in logical coordinates (compositor space)
    mla_int32_t w = (mla_int32_t)size.width;
    mla_int32_t h = (mla_int32_t)size.height;

    if (w > 0 && h > 0) {
        ws->pendingWidth = w;
        ws->pendingHeight = h;
    }

    return true;
}

mla_ui_surface_input_states_t __linux_wayland_surface_input_states(const mla_ui_surface_t &surface) {
    mla_ui_surface_input_states_t states = mla_ui_surface_input_states_empty();

    mla_linux_wayland_surface_t *ws = static_cast<mla_linux_wayland_surface_t *>(surface.resource);
    if (ws == nullptr || !ws->is_initialized) return states;

    states.cursorPosition.x = ws->input.pointerX;
    states.cursorPosition.y = ws->input.pointerY;
    states.leftMouseButtonDown = ws->input.leftButtonDown;
    states.rightMouseButtonDown = ws->input.rightButtonDown;
    states.middleMouseButtonDown = ws->input.middleButtonDown;
    states.shiftKeyDown = ws->input.shiftDown;
    states.ctrlKeyDown = ws->input.ctrlDown;
    states.altKeyDown = ws->input.altDown;
    states.metaKeyDown = ws->input.metaDown;
    states.keyCodeDown = ws->input.lastKeyCode;

    return states;
}

mla_ui_surface_draw_size_t __linux_wayland_surface_calc_text_size(const mla_ui_surface_t &surface,
                                                                    const mla_ui_surface_font_type_t &font_type,
                                                                    const mla_string_t &text) {
    mla_ui_surface_draw_size_t size = {0, 0};

    if (mla_string_is_empty(text) || g_ft_library == nullptr) return size;

    mla_linux_wayland_surface_t *ws = static_cast<mla_linux_wayland_surface_t *>(surface.resource);
    if (ws == nullptr) return size;

    FT_Face face = __linux_wayland_font_cache_get_or_create(ws->renderCache.fontCache, font_type);
    if (!face) return size;

    const mla_char_t *textData = mla_string_data(text);
    mla_size_t textLen = mla_string_length(text);

    // Font is rendered at scaled DPI, so the pixel metrics are in physical pixels.
    // Convert back to logical coordinates by dividing by the scale factor.
    mla_ui_surface_draw_size_t physicalSize = __linux_wayland_calc_text_size_impl(face, textData, textLen);
    mla_int32_t scale = ws->bufferScale;
    if (scale < 1) scale = 1;
    size.width = physicalSize.width / (mla_double_t)scale;
    size.height = physicalSize.height / (mla_double_t)scale;
    return size;
}

