// Flatten a quadratic bezier curve into line segments
static void __linux_wayland_flatten_quadratic_bezier(
    mla_double_t x0, mla_double_t y0,
    mla_double_t cpx, mla_double_t cpy,
    mla_double_t x1, mla_double_t y1,
    mla_double_t *outX, mla_double_t *outY, mla_size_t *outCount, mla_size_t maxCount) {

    mla_size_t segments = mla_global_ui_surface_linux_wayland_bezier_segments;
    if (*outCount + segments + 1 > maxCount) segments = maxCount - *outCount - 1;

    for (mla_size_t i = 0; i <= segments; i++) {
        mla_double_t t = (mla_double_t)i / (mla_double_t)segments;
        mla_double_t mt = 1.0 - t;
        outX[*outCount] = mt * mt * x0 + 2.0 * mt * t * cpx + t * t * x1;
        outY[*outCount] = mt * mt * y0 + 2.0 * mt * t * cpy + t * t * y1;
        (*outCount)++;
    }
}

// Flatten a cubic bezier curve into line segments
static void __linux_wayland_flatten_cubic_bezier(
    mla_double_t x0, mla_double_t y0,
    mla_double_t cp1x, mla_double_t cp1y,
    mla_double_t cp2x, mla_double_t cp2y,
    mla_double_t x1, mla_double_t y1,
    mla_double_t *outX, mla_double_t *outY, mla_size_t *outCount, mla_size_t maxCount) {

    mla_size_t segments = mla_global_ui_surface_linux_wayland_bezier_segments;
    if (*outCount + segments + 1 > maxCount) segments = maxCount - *outCount - 1;

    for (mla_size_t i = 0; i <= segments; i++) {
        mla_double_t t = (mla_double_t)i / (mla_double_t)segments;
        mla_double_t mt = 1.0 - t;
        mla_double_t mt2 = mt * mt;
        mla_double_t mt3 = mt2 * mt;
        mla_double_t t2 = t * t;
        mla_double_t t3 = t2 * t;

        outX[*outCount] = mt3 * x0 + 3.0 * mt2 * t * cp1x + 3.0 * mt * t2 * cp2x + t3 * x1;
        outY[*outCount] = mt3 * y0 + 3.0 * mt2 * t * cp1y + 3.0 * mt * t2 * cp2y + t3 * y1;
        (*outCount)++;
    }
}

// SVG arc endpoint parameterization to center parameterization conversion + flatten to line segments
static void __linux_wayland_flatten_arc(
    mla_double_t x0, mla_double_t y0,
    mla_double_t rx, mla_double_t ry,
    mla_double_t xAxisRotation,
    mla_bool_t largeArcFlag, mla_bool_t sweepFlag,
    mla_double_t x1, mla_double_t y1,
    mla_double_t *outX, mla_double_t *outY, mla_size_t *outCount, mla_size_t maxCount) {

    if (rx == 0.0 || ry == 0.0) {
        if (*outCount < maxCount) { outX[*outCount] = x1; outY[*outCount] = y1; (*outCount)++; }
        return;
    }

    rx = __linux_wayland_abs(rx);
    ry = __linux_wayland_abs(ry);

    mla_double_t phi = xAxisRotation * mla_math_pi / 180.0;
    mla_double_t cosPhi = mla_math_cos(phi);
    mla_double_t sinPhi = mla_math_sin(phi);

    // Step 1: Compute (x1', y1')
    mla_double_t dx2 = (x0 - x1) / 2.0;
    mla_double_t dy2 = (y0 - y1) / 2.0;
    mla_double_t x1p = cosPhi * dx2 + sinPhi * dy2;
    mla_double_t y1p = -sinPhi * dx2 + cosPhi * dy2;

    // Step 2: Compute (cx', cy')
    mla_double_t rx2 = rx * rx;
    mla_double_t ry2 = ry * ry;
    mla_double_t x1p2 = x1p * x1p;
    mla_double_t y1p2 = y1p * y1p;

    // Ensure radii are large enough
    mla_double_t lambda = x1p2 / rx2 + y1p2 / ry2;
    if (lambda > 1.0) {
        mla_double_t lambdaSqrt = mla_math_sqrt(lambda);
        rx *= lambdaSqrt;
        ry *= lambdaSqrt;
        rx2 = rx * rx;
        ry2 = ry * ry;
    }

    mla_double_t num = rx2 * ry2 - rx2 * y1p2 - ry2 * x1p2;
    mla_double_t den = rx2 * y1p2 + ry2 * x1p2;
    mla_double_t sq = 0.0;
    if (den > 0.0 && num > 0.0) {
        sq = mla_math_sqrt(num / den);
    }
    if (largeArcFlag == sweepFlag) sq = -sq;

    mla_double_t cxp = sq * rx * y1p / ry;
    mla_double_t cyp = sq * (-ry) * x1p / rx;

    // Step 3: Compute (cx, cy)
    mla_double_t cx = cosPhi * cxp - sinPhi * cyp + (x0 + x1) / 2.0;
    mla_double_t cy = sinPhi * cxp + cosPhi * cyp + (y0 + y1) / 2.0;

    // Step 4: Compute angles
    mla_double_t ux = (x1p - cxp) / rx;
    mla_double_t uy = (y1p - cyp) / ry;
    mla_double_t vx = (-x1p - cxp) / rx;
    mla_double_t vy = (-y1p - cyp) / ry;

    mla_double_t theta1 = mla_math_atan(uy, ux);
    mla_double_t dtheta = mla_math_atan(vy, vx) - theta1;

    if (sweepFlag && dtheta < 0) dtheta += 2.0 * mla_math_pi;
    else if (!sweepFlag && dtheta > 0) dtheta -= 2.0 * mla_math_pi;

    // Step 5: Generate points along the arc
    mla_size_t segments = mla_global_ui_surface_linux_wayland_arc_segments;
    if (*outCount + segments + 1 > maxCount) segments = maxCount - *outCount - 1;

    for (mla_size_t i = 1; i <= segments; i++) {
        mla_double_t t = (mla_double_t)i / (mla_double_t)segments;
        mla_double_t angle = theta1 + t * dtheta;
        mla_double_t px = rx * mla_math_cos(angle);
        mla_double_t py = ry * mla_math_sin(angle);

        outX[*outCount] = cosPhi * px - sinPhi * py + cx;
        outY[*outCount] = sinPhi * px + cosPhi * py + cy;
        (*outCount)++;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Software Rasterizer - Text Rendering
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static void __linux_wayland_draw_text(mla_uint32_t *data, mla_int32_t bufWidth, mla_int32_t bufHeight,
                                        FT_Face face, const mla_char_t *text, mla_size_t textLen,
                                        mla_double_t x, mla_double_t y, mla_uint32_t color) {
    if (!face || !text || textLen == 0) return;

    // Get premultiplied color components
    mla_uint32_t cr = (color >> 16) & 0xFF;
    mla_uint32_t cg = (color >> 8) & 0xFF;
    mla_uint32_t cb = (color) & 0xFF;

    // y is the top of the text bounding box; calculate baseline
    // Use ascender as baseline offset
    mla_double_t ascender = (mla_double_t)face->size->metrics.ascender / 64.0;
    mla_int32_t penX = (mla_int32_t)(x * 64.0); // 26.6 fixed point
    mla_int32_t baselineY = (mla_int32_t)(y + ascender);

    for (mla_size_t i = 0; i < textLen; ) {
        // Decode UTF-8 to Unicode codepoint
        mla_uint32_t codepoint = 0;
        mla_uint8_t byte = (mla_uint8_t)text[i];

        if (byte < 0x80) {
            codepoint = byte;
            i += 1;
        } else if ((byte & 0xE0) == 0xC0) {
            codepoint = byte & 0x1F;
            if (i + 1 < textLen) codepoint = (codepoint << 6) | ((mla_uint8_t)text[i + 1] & 0x3F);
            i += 2;
        } else if ((byte & 0xF0) == 0xE0) {
            codepoint = byte & 0x0F;
            if (i + 1 < textLen) codepoint = (codepoint << 6) | ((mla_uint8_t)text[i + 1] & 0x3F);
            if (i + 2 < textLen) codepoint = (codepoint << 6) | ((mla_uint8_t)text[i + 2] & 0x3F);
            i += 3;
        } else if ((byte & 0xF8) == 0xF0) {
            codepoint = byte & 0x07;
            if (i + 1 < textLen) codepoint = (codepoint << 6) | ((mla_uint8_t)text[i + 1] & 0x3F);
            if (i + 2 < textLen) codepoint = (codepoint << 6) | ((mla_uint8_t)text[i + 2] & 0x3F);
            if (i + 3 < textLen) codepoint = (codepoint << 6) | ((mla_uint8_t)text[i + 3] & 0x3F);
            i += 4;
        } else {
            i += 1;
            continue;
        }

        FT_UInt glyphIndex = FT_Get_Char_Index(face, codepoint);
        if (FT_Load_Glyph(face, glyphIndex, FT_LOAD_RENDER) != 0) {
            continue;
        }

        FT_GlyphSlot glyph = face->glyph;
        FT_Bitmap *bitmap = &glyph->bitmap;

        mla_int32_t glyphX = (penX >> 6) + glyph->bitmap_left;
        mla_int32_t glyphY = baselineY - glyph->bitmap_top;

        // Blit glyph bitmap to surface with alpha blending
        for (mla_uint32_t gy = 0; gy < bitmap->rows; gy++) {
            mla_int32_t py = glyphY + (mla_int32_t)gy;
            if (py < 0 || py >= bufHeight) continue;

            for (mla_uint32_t gx = 0; gx < bitmap->width; gx++) {
                mla_int32_t px = glyphX + (mla_int32_t)gx;
                if (px < 0 || px >= bufWidth) continue;

                mla_uint8_t alpha = bitmap->buffer[gy * (mla_uint32_t)bitmap->pitch + gx];
                if (alpha == 0) continue;

                // Modulate glyph alpha with text color alpha
                mla_uint32_t a = ((color >> 24) & 0xFF);
                mla_uint32_t glyphAlpha = (alpha * a + 127) / 255;

                // Pre-multiply color by glyph alpha
                mla_uint32_t pr = (cr * glyphAlpha + 127) / 255;
                mla_uint32_t pg = (cg * glyphAlpha + 127) / 255;
                mla_uint32_t pb = (cb * glyphAlpha + 127) / 255;

                mla_uint32_t srcPixel = (glyphAlpha << 24) | (pr << 16) | (pg << 8) | pb;
                mla_uint32_t *dstPixel = &data[py * bufWidth + px];
                *dstPixel = __linux_wayland_blend_pixel(*dstPixel, srcPixel);
            }
        }

        penX += (mla_int32_t)glyph->advance.x; // advance is in 26.6 fixed point
    }
}

// Calculate text size
static mla_ui_surface_draw_size_t __linux_wayland_calc_text_size_impl(FT_Face face, const mla_char_t *text, mla_size_t textLen) {
    mla_ui_surface_draw_size_t size = {0.0, 0.0};
    if (!face || !text || textLen == 0) return size;

    mla_int32_t penX = 0; // 26.6 fixed point

    for (mla_size_t i = 0; i < textLen; ) {
        mla_uint32_t codepoint = 0;
        mla_uint8_t byte = (mla_uint8_t)text[i];

        if (byte < 0x80) { codepoint = byte; i += 1; }
        else if ((byte & 0xE0) == 0xC0) {
            codepoint = byte & 0x1F;
            if (i + 1 < textLen) codepoint = (codepoint << 6) | ((mla_uint8_t)text[i + 1] & 0x3F);
            i += 2;
        } else if ((byte & 0xF0) == 0xE0) {
            codepoint = byte & 0x0F;
            if (i + 1 < textLen) codepoint = (codepoint << 6) | ((mla_uint8_t)text[i + 1] & 0x3F);
            if (i + 2 < textLen) codepoint = (codepoint << 6) | ((mla_uint8_t)text[i + 2] & 0x3F);
            i += 3;
        } else if ((byte & 0xF8) == 0xF0) {
            codepoint = byte & 0x07;
            if (i + 1 < textLen) codepoint = (codepoint << 6) | ((mla_uint8_t)text[i + 1] & 0x3F);
            if (i + 2 < textLen) codepoint = (codepoint << 6) | ((mla_uint8_t)text[i + 2] & 0x3F);
            if (i + 3 < textLen) codepoint = (codepoint << 6) | ((mla_uint8_t)text[i + 3] & 0x3F);
            i += 4;
        } else { i += 1; continue; }

        FT_UInt glyphIndex = FT_Get_Char_Index(face, codepoint);
        if (FT_Load_Glyph(face, glyphIndex, FT_LOAD_DEFAULT) != 0) continue;

        penX += (mla_int32_t)face->glyph->advance.x;
    }

    size.width = (mla_double_t)(penX >> 6);
    size.height = (mla_double_t)(face->size->metrics.height >> 6);
    return size;
}


