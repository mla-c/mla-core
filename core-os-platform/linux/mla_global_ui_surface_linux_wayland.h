// Fast horizontal line fill
static void __linux_wayland_fill_hline(mla_uint32_t *data, mla_int32_t bufWidth, mla_int32_t bufHeight,
                                         mla_int32_t x0, mla_int32_t x1, mla_int32_t y, mla_uint32_t color) {
    if (y < 0 || y >= bufHeight) return;
    if (x0 > x1) { mla_int32_t tmp = x0; x0 = x1; x1 = tmp; }
    if (x0 < 0) x0 = 0;
    if (x1 >= bufWidth) x1 = bufWidth - 1;
    if (x0 > x1) return;

    mla_uint32_t sa = (color >> 24) & 0xFF;
    mla_uint32_t *row = data + y * bufWidth;

    if (sa == 255) {
        // Opaque fast path: directly set pixels
        for (mla_int32_t x = x0; x <= x1; x++) {
            row[x] = color;
        }
    } else if (sa > 0) {
        for (mla_int32_t x = x0; x <= x1; x++) {
            row[x] = __linux_wayland_blend_pixel(row[x], color);
        }
    }
}

// Fill rectangle (axis-aligned)
static void __linux_wayland_fill_rect(mla_uint32_t *data, mla_int32_t bufWidth, mla_int32_t bufHeight,
                                        mla_double_t x, mla_double_t y, mla_double_t w, mla_double_t h,
                                        mla_uint32_t color) {
    mla_int32_t ix0 = (mla_int32_t)x;
    mla_int32_t iy0 = (mla_int32_t)y;
    mla_int32_t ix1 = (mla_int32_t)(x + w);
    mla_int32_t iy1 = (mla_int32_t)(y + h);

    ix0 = __linux_wayland_iclamp(ix0, 0, bufWidth);
    ix1 = __linux_wayland_iclamp(ix1, 0, bufWidth);
    iy0 = __linux_wayland_iclamp(iy0, 0, bufHeight);
    iy1 = __linux_wayland_iclamp(iy1, 0, bufHeight);

    for (mla_int32_t row = iy0; row < iy1; row++) {
        __linux_wayland_fill_hline(data, bufWidth, bufHeight, ix0, ix1 - 1, row, color);
    }
}

// Fill rounded rectangle
static void __linux_wayland_fill_rounded_rect(mla_uint32_t *data, mla_int32_t bufWidth, mla_int32_t bufHeight,
                                                mla_double_t x, mla_double_t y, mla_double_t w, mla_double_t h,
                                                mla_double_t rx, mla_double_t ry, mla_uint32_t color) {
    if (rx <= 0.0 && ry <= 0.0) {
        __linux_wayland_fill_rect(data, bufWidth, bufHeight, x, y, w, h, color);
        return;
    }

    // Clamp radii
    if (rx > w / 2.0) rx = w / 2.0;
    if (ry > h / 2.0) ry = h / 2.0;

    mla_int32_t iy0 = (mla_int32_t)y;
    mla_int32_t iy1 = (mla_int32_t)(y + h);
    iy0 = __linux_wayland_iclamp(iy0, 0, bufHeight);
    iy1 = __linux_wayland_iclamp(iy1, 0, bufHeight);

    for (mla_int32_t row = iy0; row < iy1; row++) {
        mla_double_t dy = (mla_double_t)row + 0.5;
        mla_double_t leftX = x;
        mla_double_t rightX = x + w;

        // Top corners
        if (dy < y + ry) {
            mla_double_t t = (y + ry - dy) / ry;
            mla_double_t offset = rx * (1.0 - mla_math_sqrt(1.0 - t * t));
            leftX = x + offset;
            rightX = x + w - offset;
        }
        // Bottom corners
        else if (dy > y + h - ry) {
            mla_double_t t = (dy - (y + h - ry)) / ry;
            mla_double_t offset = rx * (1.0 - mla_math_sqrt(1.0 - t * t));
            leftX = x + offset;
            rightX = x + w - offset;
        }

        __linux_wayland_fill_hline(data, bufWidth, bufHeight,
                                    (mla_int32_t)leftX, (mla_int32_t)rightX - 1, row, color);
    }
}

// Stroke rectangle outline
static void __linux_wayland_stroke_rect(mla_uint32_t *data, mla_int32_t bufWidth, mla_int32_t bufHeight,
                                          mla_double_t x, mla_double_t y, mla_double_t w, mla_double_t h,
                                          mla_double_t rx, mla_double_t ry, mla_double_t strokeWidth,
                                          mla_uint32_t strokeColor);

// Fill ellipse
static void __linux_wayland_fill_ellipse(mla_uint32_t *data, mla_int32_t bufWidth, mla_int32_t bufHeight,
                                           mla_double_t cx, mla_double_t cy, mla_double_t rx, mla_double_t ry,
                                           mla_uint32_t color) {
    if (rx <= 0.0 || ry <= 0.0) return;

    mla_int32_t iy0 = (mla_int32_t)(cy - ry);
    mla_int32_t iy1 = (mla_int32_t)(cy + ry) + 1;
    iy0 = __linux_wayland_iclamp(iy0, 0, bufHeight);
    iy1 = __linux_wayland_iclamp(iy1, 0, bufHeight);

    for (mla_int32_t row = iy0; row < iy1; row++) {
        mla_double_t dy = ((mla_double_t)row + 0.5 - cy) / ry;
        if (dy * dy > 1.0) continue;
        mla_double_t dx = rx * mla_math_sqrt(1.0 - dy * dy);
        mla_int32_t ix0 = (mla_int32_t)(cx - dx);
        mla_int32_t ix1 = (mla_int32_t)(cx + dx);
        __linux_wayland_fill_hline(data, bufWidth, bufHeight, ix0, ix1, row, color);
    }
}

// Draw anti-aliased line using Wu's algorithm with thickness support
static void __linux_wayland_draw_line(mla_uint32_t *data, mla_int32_t bufWidth, mla_int32_t bufHeight,
                                        mla_double_t x0, mla_double_t y0, mla_double_t x1, mla_double_t y1,
                                        mla_double_t thickness, mla_uint32_t color) {
    if (thickness <= 0.0) return;

    if (thickness <= 1.5) {
        // Thin line: Wu's algorithm
        mla_bool_t steep = __linux_wayland_abs(y1 - y0) > __linux_wayland_abs(x1 - x0);

        if (steep) {
            mla_double_t tmp;
            tmp = x0; x0 = y0; y0 = tmp;
            tmp = x1; x1 = y1; y1 = tmp;
        }
        if (x0 > x1) {
            mla_double_t tmp;
            tmp = x0; x0 = x1; x1 = tmp;
            tmp = y0; y0 = y1; y1 = tmp;
        }

        mla_double_t dx = x1 - x0;
        mla_double_t dy = y1 - y0;
        mla_double_t gradient = (dx == 0.0) ? 1.0 : dy / dx;

        // First endpoint
        mla_int32_t xpxl1 = (mla_int32_t)(x0 + 0.5);
        mla_double_t yend = y0 + gradient * ((mla_double_t)xpxl1 - x0);
        mla_double_t intery = yend + gradient;

        // Second endpoint
        mla_int32_t xpxl2 = (mla_int32_t)(x1 + 0.5);

        for (mla_int32_t x = xpxl1; x <= xpxl2; x++) {
            mla_int32_t iy = (mla_int32_t)intery;
            mla_double_t frac = intery - (mla_double_t)iy;
            mla_uint32_t cov1 = (mla_uint32_t)((1.0 - frac) * 255.0);
            mla_uint32_t cov2 = (mla_uint32_t)(frac * 255.0);

            if (steep) {
                __linux_wayland_set_pixel_aa(data, bufWidth, bufHeight, iy, x, color, cov1);
                __linux_wayland_set_pixel_aa(data, bufWidth, bufHeight, iy + 1, x, color, cov2);
            } else {
                __linux_wayland_set_pixel_aa(data, bufWidth, bufHeight, x, iy, color, cov1);
                __linux_wayland_set_pixel_aa(data, bufWidth, bufHeight, x, iy + 1, color, cov2);
            }
            intery += gradient;
        }
    } else {
        // Thick line: draw as filled rectangle along the line
        mla_double_t dx = x1 - x0;
        mla_double_t dy = y1 - y0;
        mla_double_t len = mla_math_sqrt(dx * dx + dy * dy);
        if (len < 0.001) return;

        mla_double_t nx = -dy / len * (thickness / 2.0);
        mla_double_t ny = dx / len * (thickness / 2.0);

        // Four corners of the thick line quad
        mla_double_t qx[4], qy[4];
        qx[0] = x0 + nx; qy[0] = y0 + ny;
        qx[1] = x0 - nx; qy[1] = y0 - ny;
        qx[2] = x1 - nx; qy[2] = y1 - ny;
        qx[3] = x1 + nx; qy[3] = y1 + ny;

        // Find bounding box
        mla_double_t minY = qy[0], maxY = qy[0];
        for (mla_int32_t i = 1; i < 4; i++) {
            if (qy[i] < minY) minY = qy[i];
            if (qy[i] > maxY) maxY = qy[i];
        }

        mla_int32_t iy0 = __linux_wayland_iclamp((mla_int32_t)minY, 0, bufHeight);
        mla_int32_t iy1 = __linux_wayland_iclamp((mla_int32_t)maxY + 1, 0, bufHeight);

        // Scanline fill the quad
        for (mla_int32_t row = iy0; row < iy1; row++) {
            mla_double_t scanY = (mla_double_t)row + 0.5;
            mla_double_t intersections[8];
            mla_int32_t numIntersections = 0;

            for (mla_int32_t i = 0; i < 4; i++) {
                mla_int32_t j = (i + 1) % 4;
                mla_double_t ey0 = qy[i], ey1 = qy[j];
                if ((ey0 <= scanY && ey1 > scanY) || (ey1 <= scanY && ey0 > scanY)) {
                    mla_double_t t = (scanY - ey0) / (ey1 - ey0);
                    intersections[numIntersections++] = qx[i] + t * (qx[j] - qx[i]);
                }
            }

            if (numIntersections >= 2) {
                if (intersections[0] > intersections[1]) {
                    mla_double_t tmp = intersections[0];
                    intersections[0] = intersections[1];
                    intersections[1] = tmp;
                }
                __linux_wayland_fill_hline(data, bufWidth, bufHeight,
                                            (mla_int32_t)intersections[0], (mla_int32_t)intersections[1], row, color);
            }
        }
    }
}

// Stroke ellipse outline
static void __linux_wayland_stroke_ellipse(mla_uint32_t *data, mla_int32_t bufWidth, mla_int32_t bufHeight,
                                             mla_double_t cx, mla_double_t cy, mla_double_t rx, mla_double_t ry,
                                             mla_double_t strokeWidth, mla_uint32_t strokeColor) {
    // Draw outline by filling between two ellipses
    mla_double_t outerRx = rx + strokeWidth / 2.0;
    mla_double_t outerRy = ry + strokeWidth / 2.0;
    mla_double_t innerRx = rx - strokeWidth / 2.0;
    mla_double_t innerRy = ry - strokeWidth / 2.0;

    if (innerRx < 0) innerRx = 0;
    if (innerRy < 0) innerRy = 0;

    mla_int32_t iy0 = (mla_int32_t)(cy - outerRy);
    mla_int32_t iy1 = (mla_int32_t)(cy + outerRy) + 1;
    iy0 = __linux_wayland_iclamp(iy0, 0, bufHeight);
    iy1 = __linux_wayland_iclamp(iy1, 0, bufHeight);

    for (mla_int32_t row = iy0; row < iy1; row++) {
        mla_double_t dy = ((mla_double_t)row + 0.5 - cy);

        // Outer ellipse bounds
        mla_double_t tOuter = dy / outerRy;
        if (tOuter * tOuter > 1.0) continue;
        mla_double_t dxOuter = outerRx * mla_math_sqrt(1.0 - tOuter * tOuter);

        // Inner ellipse bounds
        mla_double_t dxInner = 0.0;
        if (innerRx > 0 && innerRy > 0) {
            mla_double_t tInner = dy / innerRy;
            if (tInner * tInner < 1.0) {
                dxInner = innerRx * mla_math_sqrt(1.0 - tInner * tInner);
            }
        }

        // Left stroke span
        __linux_wayland_fill_hline(data, bufWidth, bufHeight,
                                    (mla_int32_t)(cx - dxOuter), (mla_int32_t)(cx - dxInner), row, strokeColor);
        // Right stroke span
        __linux_wayland_fill_hline(data, bufWidth, bufHeight,
                                    (mla_int32_t)(cx + dxInner), (mla_int32_t)(cx + dxOuter), row, strokeColor);
    }
}

// Stroke rectangle outline (deferred implementation)
static void __linux_wayland_stroke_rect(mla_uint32_t *data, mla_int32_t bufWidth, mla_int32_t bufHeight,
                                          mla_double_t x, mla_double_t y, mla_double_t w, mla_double_t h,
                                          mla_double_t rx, mla_double_t ry, mla_double_t strokeWidth,
                                          mla_uint32_t strokeColor) {
    if (strokeWidth <= 0.0) return;
    mla_double_t hw = strokeWidth / 2.0;

    if (rx <= 0.0 && ry <= 0.0) {
        // Top edge
        __linux_wayland_fill_rect(data, bufWidth, bufHeight, x - hw, y - hw, w + strokeWidth, strokeWidth, strokeColor);
        // Bottom edge
        __linux_wayland_fill_rect(data, bufWidth, bufHeight, x - hw, y + h - hw, w + strokeWidth, strokeWidth, strokeColor);
        // Left edge
        __linux_wayland_fill_rect(data, bufWidth, bufHeight, x - hw, y + hw, strokeWidth, h - strokeWidth, strokeColor);
        // Right edge
        __linux_wayland_fill_rect(data, bufWidth, bufHeight, x + w - hw, y + hw, strokeWidth, h - strokeWidth, strokeColor);
    } else {
        // Rounded rect stroke: scanline-based band between outer and inner rounded rects
        mla_double_t outerX = x - hw, outerY = y - hw;
        mla_double_t outerW = w + strokeWidth, outerH = h + strokeWidth;
        mla_double_t outerRx = rx + hw, outerRy = ry + hw;

        mla_double_t innerX = x + hw, innerY = y + hw;
        mla_double_t innerW = w - strokeWidth, innerH = h - strokeWidth;
        mla_double_t innerRx = rx - hw, innerRy = ry - hw;
        if (innerRx < 0) innerRx = 0;
        if (innerRy < 0) innerRy = 0;
        if (innerW < 0) innerW = 0;
        if (innerH < 0) innerH = 0;

        // Clamp outer radii
        if (outerRx > outerW / 2.0) outerRx = outerW / 2.0;
        if (outerRy > outerH / 2.0) outerRy = outerH / 2.0;
        if (innerRx > innerW / 2.0) innerRx = innerW / 2.0;
        if (innerRy > innerH / 2.0) innerRy = innerH / 2.0;

        mla_int32_t rowStart = __linux_wayland_iclamp((mla_int32_t)outerY, 0, bufHeight);
        mla_int32_t rowEnd = __linux_wayland_iclamp((mla_int32_t)(outerY + outerH) + 1, 0, bufHeight);

        for (mla_int32_t row = rowStart; row < rowEnd; row++) {
            mla_double_t dy = (mla_double_t)row + 0.5;

            // Compute outer rounded rect span
            mla_double_t oLeft = outerX, oRight = outerX + outerW;
            if (dy < outerY + outerRy) {
                mla_double_t t = (outerY + outerRy - dy) / outerRy;
                mla_double_t off = outerRx * (1.0 - mla_math_sqrt(__linux_wayland_max(0.0, 1.0 - t * t)));
                oLeft = outerX + off;
                oRight = outerX + outerW - off;
            } else if (dy > outerY + outerH - outerRy) {
                mla_double_t t = (dy - (outerY + outerH - outerRy)) / outerRy;
                mla_double_t off = outerRx * (1.0 - mla_math_sqrt(__linux_wayland_max(0.0, 1.0 - t * t)));
                oLeft = outerX + off;
                oRight = outerX + outerW - off;
            }

            // Compute inner rounded rect span (if inside inner rect)
            mla_bool_t hasInner = (innerW > 0 && innerH > 0 && dy >= innerY && dy <= innerY + innerH);
            mla_double_t iLeft = innerX, iRight = innerX + innerW;
            if (hasInner) {
                if (dy < innerY + innerRy) {
                    mla_double_t t = (innerY + innerRy - dy) / (innerRy > 0 ? innerRy : 1.0);
                    if (t * t < 1.0) {
                        mla_double_t off = innerRx * (1.0 - mla_math_sqrt(1.0 - t * t));
                        iLeft = innerX + off;
                        iRight = innerX + innerW - off;
                    } else {
                        hasInner = false;
                    }
                } else if (dy > innerY + innerH - innerRy) {
                    mla_double_t t = (dy - (innerY + innerH - innerRy)) / (innerRy > 0 ? innerRy : 1.0);
                    if (t * t < 1.0) {
                        mla_double_t off = innerRx * (1.0 - mla_math_sqrt(1.0 - t * t));
                        iLeft = innerX + off;
                        iRight = innerX + innerW - off;
                    } else {
                        hasInner = false;
                    }
                }
            }

            if (hasInner && iLeft < iRight) {
                // Draw left and right stroke bands
                __linux_wayland_fill_hline(data, bufWidth, bufHeight,
                    (mla_int32_t)oLeft, (mla_int32_t)iLeft, row, strokeColor);
                __linux_wayland_fill_hline(data, bufWidth, bufHeight,
                    (mla_int32_t)iRight, (mla_int32_t)oRight, row, strokeColor);
            } else {
                // No inner rect at this scanline - fill entire outer span
                __linux_wayland_fill_hline(data, bufWidth, bufHeight,
                    (mla_int32_t)oLeft, (mla_int32_t)oRight, row, strokeColor);
            }
        }
    }
}


