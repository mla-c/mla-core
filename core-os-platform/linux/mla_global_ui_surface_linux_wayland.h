// Keyboard listeners
static void __linux_wayland_keyboard_keymap(void *data, struct wl_keyboard *keyboard,
                                              mla_uint32_t format, mla_int32_t fd, mla_uint32_t size) {
    (void)data; (void)keyboard;
    mla_linux_wayland_surface_t *ws = __linux_wayland_active_surface;
    if (!ws) { close(fd); return; }

    if (format != WL_KEYBOARD_KEYMAP_FORMAT_XKB_V1) { close(fd); return; }

    mla_char_t *mapStr = static_cast<mla_char_t *>(mmap(nullptr, size, PROT_READ, MAP_PRIVATE, fd, 0));
    if (mapStr == MAP_FAILED) { close(fd); return; }

    if (ws->input.xkbKeymap) { xkb_keymap_unref(ws->input.xkbKeymap); ws->input.xkbKeymap = nullptr; }
    if (ws->input.xkbState) { xkb_state_unref(ws->input.xkbState); ws->input.xkbState = nullptr; }

    ws->input.xkbKeymap = xkb_keymap_new_from_string(ws->input.xkbContext, mapStr,
                                                        XKB_KEYMAP_FORMAT_TEXT_V1,
                                                        XKB_KEYMAP_COMPILE_NO_FLAGS);
    munmap(mapStr, size);
    close(fd);

    if (ws->input.xkbKeymap) {
        ws->input.xkbState = xkb_state_new(ws->input.xkbKeymap);
    }
}

